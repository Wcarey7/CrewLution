-- CrewLution PostgreSQL DDL
-- Domain schema for accounts, crm, and commerce apps.
--
-- Source: Django models and migrations (as of 2026-06-08).
-- Related: docs/schema.dbml, docs/database-schema.md
--
-- Notes:
--   * Django uses BIGINT identity PKs (BigAutoField).
--   * auth_user is included because domain tables reference it.
--     Full Django install also creates auth_group, django_session, etc. via migrate.
--   * Human-readable references (QT-00001, JB-00001, INV-00001) are computed in
--     application code from per-company sequence columns.
--   * line_item.amount is computed as quantity * unit_price (not stored).

BEGIN;

-- ─── Django auth (subset) ─────────────────────────────────────────────────────

CREATE TABLE auth_user (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    password        VARCHAR(128) NOT NULL,
    last_login      TIMESTAMPTZ,
    is_superuser    BOOLEAN NOT NULL DEFAULT FALSE,
    username        VARCHAR(150) NOT NULL,
    first_name      VARCHAR(150) NOT NULL DEFAULT '',
    last_name       VARCHAR(150) NOT NULL DEFAULT '',
    email           VARCHAR(254) NOT NULL DEFAULT '',
    is_staff        BOOLEAN NOT NULL DEFAULT FALSE,
    is_active       BOOLEAN NOT NULL DEFAULT TRUE,
    date_joined     TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT auth_user_username_key UNIQUE (username)
);

COMMENT ON TABLE auth_user IS 'Django built-in user model (subset for FK references).';

-- ─── accounts ─────────────────────────────────────────────────────────────────

CREATE TABLE accounts_company (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    name                VARCHAR(200) NOT NULL,
    phone               VARCHAR(50) NOT NULL DEFAULT '',
    website             VARCHAR(200) NOT NULL DEFAULT '',
    email               VARCHAR(254) NOT NULL DEFAULT '',
    address1            VARCHAR(200) NOT NULL DEFAULT '',
    address2            VARCHAR(200) NOT NULL DEFAULT '',
    city                VARCHAR(120) NOT NULL DEFAULT '',
    state               VARCHAR(120) NOT NULL DEFAULT '',
    postal_code         VARCHAR(40) NOT NULL DEFAULT '',
    business_hours      JSONB NOT NULL DEFAULT '{}'::jsonb,
    show_business_hours BOOLEAN NOT NULL DEFAULT FALSE,
    is_active           BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE INDEX accounts_company_created_at_idx ON accounts_company (created_at);
CREATE INDEX accounts_company_updated_at_idx ON accounts_company (updated_at);
CREATE INDEX accounts_company_name_idx ON accounts_company (name);
CREATE INDEX accounts_company_is_active_idx ON accounts_company (is_active);

COMMENT ON TABLE accounts_company IS 'Tenant root entity.';

