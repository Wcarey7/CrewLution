    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    company_id  BIGINT NOT NULL REFERENCES accounts_company (id) ON DELETE CASCADE,
    customer_id BIGINT NOT NULL REFERENCES crm_customer (id) ON DELETE CASCADE,
    sequence    INTEGER NOT NULL DEFAULT 0,
    title       VARCHAR(200) NOT NULL DEFAULT '',
    status      VARCHAR(20) NOT NULL DEFAULT 'draft',
    total       NUMERIC(12, 2) NOT NULL DEFAULT 0.00,
    currency    VARCHAR(3) NOT NULL DEFAULT 'USD',
    accepted_at TIMESTAMPTZ,
    CONSTRAINT uniq_quote_company_sequence UNIQUE (company_id, sequence),
    CONSTRAINT commerce_quote_status_check CHECK (
        status IN ('draft', 'sent', 'accepted', 'declined', 'expired')
