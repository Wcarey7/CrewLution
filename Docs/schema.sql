CREATE TABLE commerce_lineitem (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    company_id  BIGINT NOT NULL REFERENCES accounts_company (id) ON DELETE CASCADE,
    quote_id    BIGINT REFERENCES commerce_quote (id) ON DELETE CASCADE,
    job_id      BIGINT REFERENCES commerce_job (id) ON DELETE CASCADE,
    invoice_id  BIGINT REFERENCES commerce_invoice (id) ON DELETE CASCADE,
    description VARCHAR(300) NOT NULL,
    quantity    NUMERIC(10, 2) NOT NULL DEFAULT 1.00,
    unit_price  NUMERIC(12, 2) NOT NULL DEFAULT 0.00,
    sort_order  INTEGER NOT NULL DEFAULT 0,
    CONSTRAINT commerce_lineitem_quantity_check CHECK (quantity >= 0.01),
    CONSTRAINT commerce_lineitem_unit_price_check CHECK (unit_price >= 0),
    CONSTRAINT commerce_lineitem_single_parent_check CHECK (
        (quote_id IS NOT NULL)::integer
        + (job_id IS NOT NULL)::integer
        + (invoice_id IS NOT NULL)::integer = 1
    )
);

CREATE INDEX commerce_lineitem_created_at_idx ON commerce_lineitem (created_at);
CREATE INDEX commerce_lineitem_updated_at_idx ON commerce_lineitem (updated_at);
CREATE INDEX commerce_lineitem_quote_id_idx ON commerce_lineitem (quote_id);
CREATE INDEX commerce_lineitem_job_id_idx ON commerce_lineitem (job_id);
CREATE INDEX commerce_lineitem_invoice_id_idx ON commerce_lineitem (invoice_id);

COMMENT ON TABLE commerce_lineitem IS 'Exactly one parent FK set. amount = quantity * unit_price (computed in app).';

